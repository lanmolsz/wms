<template>
    <div class="content-wrapper">
        <!-- Content Header (Page header) -->
        <section class="content-header">
            <h1>
                {{metadata.label}}
            </h1>
            <navigator :navigator="$root.navigator"></navigator>
        </section>

        <section class="content">
            <v-form :form="form"></v-form>
        </section>
    </div>
</template>
<style>
</style>
<script>
    import VForm from './Bootstrap/Form.vue'
    import FormCls from '../src/Form/Form';
    import Navigator from './Bootstrap/Navigator.vue';
    import formConfig from '../config/view/forms';

    export default{
        data(){
            return {
                form: FormCls.createForm(this.metadata, formConfig[this.metadata.name])
            }
        },
        components: {
            VForm,
            Navigator
        },
        props: {
            metadata: Object
        },
        events: {
            submit(form){
                let jsonData = form.toJson();

                this.$http.put(this.$route.params.name + '/' + form.idElement.value, jsonData).then((response) => {
                    console.log(response.data);
                }, (response) => {
                    // error callback
                });
            }
        },
        route: {
            data(){
                this.$root.navigator.build(this.$route);
            }
        }
    }
</script>
